package com.spectra.jewel.data;

public class ProductPriceData {
	double metalPrice;
	double stonesPrice;
	double diamondPrice;
	double labour;
	double totalPrice;
	String currency;

	public double getMetalPrice() {
		return metalPrice;
	}
	public void setMetalPrice(double metalPrice) {
		this.metalPrice = metalPrice;
	}
	public double getStonesPrice() {
		return stonesPrice;
	}
	public void setStonesPrice(double stonesprice) {
		this.stonesPrice = stonesprice;
	}
	public double getDiamondPrice() {
		return diamondPrice;
	}
	public void setDiamondPrice(double diamondPrice) {
		this.diamondPrice = diamondPrice;
	}
	public double getLabour() {
		return labour;
	}
	public void setLabour(double labour) {
		this.labour = labour;
	}
	public double getTotalPrice() {
		return totalPrice;
	}
	public void setTotalPrice(double totalPrice) {
		this.totalPrice = totalPrice;
	}
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	

}
